                    <h4>What is Health Promotion?</h4>
                    <p>Health Promotion is the process of enabling people to increase control over, and to improve, their health. The first global movement on Health Promotion came from the Ottawa Charter which was produced from the first International Conference on Health Promotion in Ottawa, Canada in 1986. </p>

                    <a class=" text-dark">
                        Three main strategies for health promotion identified:
                    </a>
                    <ol>
                        <li>Advocacy for health to create essential conditions for its. </li>
                        <li>Enabling all people to achieve full health potential. </li>
                        <li>Mediating between the different interests in society in pursuit of health. </li>
                    </ol>
                    <a class=" text-dark">
                        Health Promotion has the following priority action areas:
                    </a>
                    <ol>
                        <li>Build Healthy Public Policy</li>
                        <li>Create a supportive environment for health </li>
                        <li>Strengthen community action for health </li>
                        <li>Develop personal skills. </li>
                        <li>Re-orient health services.</li>
                    </ol>
                    <h4>Why Health Promotion Now?</h4>
                    <ol>
                        <li>High burden of non-communicable disease (NCDs) globally, particularly in developing countries. </li>
                        <li>Health promotion is the main solution for the prevention NDC burden. </li>
                        <li>Investment in health promotion and disease prevention can contribute to the achievement of sustainable development. </li>
                        <li>Universities can play a leading role in promoting healthy lifestyles and a suitable environment through a health promotion approach</li>
                    </ol>
                    <h4>How to apply for membership?</h4>
                    <p>AUN–HPN membership has two categories: full member and associate member. Currently, full members of AUN are limited to 30 universities in the ten ASEAN countries. There are no membership fees.</p>

                    <ul>
                        <li>COR-Member- Full member universities of AUN are automatically members of AUN–HPN, one of AUN’s thematic networks. </li>
                        <li>Associate member - Organizations or entities interested in joining AUN–HPN can apply to be associated members. </li>
                    </ul>
